def main():
    """Main execution function."""
    print("\n" + "🚀 " * 20)
    print("SHAP Analysis for TabPFN Regression Model")
    print("Dataset: Diabetes Progression Prediction")
    print("🚀 " * 20 + "\n")
    
    # Configuration
    TEST_SIZE = 0.2
    RANDOM_STATE = 42
    N_ESTIMATORS = 1  # Use higher (e.g., 4 or 8) for better accuracy
    N_BACKGROUND = 50
    N_TEST_SAMPLES = 50
    
    try:
        # Step 1: Load data
        X_train, X_test, y_train, y_test, feature_names = load_and_prepare_data(
            test_size=TEST_SIZE,
            random_state=RANDOM_STATE
        )
        
        # Step 2: Train model
        model = train_tabpfn_model(X_train, y_train, n_estimators=N_ESTIMATORS)
        
        # Step 3: Compute SHAP values
        shap_values, explainer, X_test_sample = compute_shap_values(
            model, X_train, X_test,
            n_background=N_BACKGROUND,
            n_test_samples=N_TEST_SAMPLES
        )
        
        # Step 4: Analyze feature importance
        feature_importance = analyze_feature_importance(
            shap_values, feature_names, X_test_sample
        )
        
        # Step 5: Generate visualizations
        saved_files = generate_visualizations(
            shap_values, X_test_sample, feature_names
        )
        
        # Step 6: Print insights
        print_insights(feature_importance)
        
        # Summary
        print("\n" + "=" * 70)
        print("Analysis Complete!")
        print("=" * 70)
        print(f"\nGenerated files:")
        for f in saved_files:
            size_kb = f.stat().st_size / 1024
            print(f"  - {f.name} ({size_kb:.1f} KB)")
        
        print(f"\n📊 Results Summary:")
        print(f"  - Model: TabPFN v2 Regressor")
        print(f"  - Most important feature: {feature_importance.iloc[0]['feature']}")
        print(f"  - SHAP value range: [{np.min(shap_values):.2f}, {np.max(shap_values):.2f}]")
        print(f"\n✅ All visualizations saved successfully!")
        
    except Exception as e:
        print(f"\n❌ Error: {e}")
        raise


if __name__ == "__main__":
    main()
