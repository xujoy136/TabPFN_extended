from __future__ import annotations

import warnings
from pathlib import Path

import matplotlib
import matplotlib.pyplot as plt
import numpy as np
import pandas as pd
import shap
from sklearn.datasets import load_diabetes
from sklearn.model_selection import train_test_split

from tabpfn import TabPFNRegressor
from tabpfn.constants import ModelVersion

# Suppress warnings for cleaner output
warnings.filterwarnings('ignore')

# Configure matplotlib
matplotlib.use('Agg')  # Non-interactive backend for saving plots
plt.style.use('default')


def load_and_prepare_data(test_size: float = 0.2, random_state: int = 42) -> tuple:
    """
    Load and prepare the diabetes dataset.
    
    Args:
        test_size: Proportion of dataset to use for testing
        random_state: Random seed for reproducibility
        
    Returns:
        Tuple of (X_train, X_test, y_train, y_test, feature_names)
    """
    print("=" * 70)
    print("Loading Diabetes Dataset")
    print("=" * 70)
    
    diabetes = load_diabetes()
    X = pd.DataFrame(diabetes.data, columns=diabetes.feature_names)
    y = diabetes.target
    
    print(f"\nDataset Info:")
    print(f"  - Total samples: {len(y)}")
    print(f"  - Number of features: {X.shape[1]}")
    print(f"  - Feature names: {list(diabetes.feature_names)}")
    print(f"\nFeature Descriptions:")
    print(f"  - age: Age in years")
    print(f"  - sex: Gender (male/female)")
    print(f"  - bmi: Body Mass Index (weight in kg/(height in m)^2)")
    print(f"  - bp: Average blood pressure")
    print(f"  - s1-s6: Six blood serum measurements")
    
    X_train, X_test, y_train, y_test = train_test_split(
        X, y, test_size=test_size, random_state=random_state
    )
    
    print(f"\nData Split:")
    print(f"  - Training samples: {len(X_train)} ({(1-test_size)*100:.0f}%)")
    print(f"  - Testing samples: {len(X_test)} ({test_size*100:.0f}%)")
    
    return X_train, X_test, y_train, y_test, diabetes.feature_names


def train_tabpfn_model(
    X_train: pd.DataFrame, 
    y_train: np.ndarray,
    n_estimators: int = 1
) -> TabPFNRegressor:
    """
    Train a TabPFN regression model.
    
    Args:
        X_train: Training features
        y_train: Training targets
        n_estimators: Number of ensemble estimators (higher = better but slower)
        
    Returns:
        Trained TabPFNRegressor model
    """
    print("\n" + "=" * 70)
    print("Training TabPFN v2 Regressor")
    print("=" * 70)
    print(f"Parameters:")
    print(f"  - Model version: v2 (no HuggingFace auth required)")
    print(f"  - n_estimators: {n_estimators}")
    print(f"  - Device: auto (will use GPU if available)")
    
    model = TabPFNRegressor.create_default_for_version(
        ModelVersion.V2,
        n_estimators=n_estimators
    )
    model.fit(X_train, y_train)
    
    print("✓ Model training completed")
    return model


def compute_shap_values(
    model: TabPFNRegressor,
    X_train: pd.DataFrame,
    X_test: pd.DataFrame,
    n_background: int = 50,
    n_test_samples: int = 50
) -> tuple:
    """
    Compute SHAP values for the model.
    
    Args:
        model: Trained TabPFN model
        X_train: Training data (for background distribution)
        X_test: Test data to explain
        n_background: Number of background samples for SHAP explainer
        n_test_samples: Number of test samples to compute SHAP values for
        
    Returns:
        Tuple of (shap_values, explainer, X_test_sample)
    """
    print("\n" + "=" * 70)
    print("Computing SHAP Values")
    print("=" * 70)
    print(f"Using KernelExplainer (model-agnostic)")
    print(f"  - Background samples: {n_background}")
    print(f"  - Test samples to explain: {n_test_samples}")
    print(f"Note: This may take a few minutes...")
    
    # Use a subset of training data as background
    background = X_train.iloc[:n_background]
    
    # Sample test data for faster computation
    X_test_sample = X_test.iloc[:n_test_samples]
    
    # Create KernelExplainer (works with any model)
    explainer = shap.KernelExplainer(model.predict, background)
    
    # Compute SHAP values
    shap_values = explainer.shap_values(X_test_sample)
    
    print(f"✓ SHAP values computed successfully")
    print(f"  - Shape: {shap_values.shape}")
    print(f"  - Expected value (baseline): {explainer.expected_value:.2f}")
    
    return shap_values, explainer, X_test_sample


def analyze_feature_importance(
    shap_values: np.ndarray,
    feature_names: list,
    X_test_sample: pd.DataFrame
) -> pd.DataFrame:
    """
    Analyze and rank feature importance based on SHAP values.
    
    Args:
        shap_values: Computed SHAP values
        feature_names: List of feature names
        X_test_sample: Test samples
        
    Returns:
        DataFrame with feature importance rankings
    """
    print("\n" + "=" * 70)
    print("Feature Importance Analysis")
    print("=" * 70)
    
    # Calculate mean absolute SHAP values for each feature
    mean_abs_shap = np.abs(shap_values).mean(axis=0)
    
    # Create DataFrame for ranking
    feature_importance = pd.DataFrame({
        'feature': feature_names,
        'mean_abs_shap': mean_abs_shap,
        'std_shap': np.std(shap_values, axis=0)
    }).sort_values('mean_abs_shap', ascending=False)
    
    print("\nRanked by |SHAP| value (higher = more important):")
    print("-" * 50)
    max_importance = feature_importance['mean_abs_shap'].max()
    
    for idx, row in feature_importance.iterrows():
        bar_length = int(row['mean_abs_shap'] / max_importance * 30)
        bar = '█' * bar_length
        print(f"{row['feature']:>10} | {bar:<30} | {row['mean_abs_shap']:>6.2f}")
    
    return feature_importance


def generate_visualizations(
    shap_values: np.ndarray,
    X_test_sample: pd.DataFrame,
    feature_names: list,
    output_dir: Path = Path('.')
) -> list[Path]:
    """
    Generate SHAP visualizations.
    
    Args:
        shap_values: Computed SHAP values
        X_test_sample: Test samples
        feature_names: List of feature names
        output_dir: Directory to save plots
        
    Returns:
        List of saved file paths
    """
    print("\n" + "=" * 70)
    print("Generating Visualizations")
    print("=" * 70)
    
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    saved_files = []
    
    # 1. Feature Importance Bar Plot
    print("\n1. Creating feature importance bar plot...")
    fig, ax = plt.subplots(figsize=(10, 6))
    shap.summary_plot(shap_values, X_test_sample, plot_type="bar", show=False)
    plt.title('TabPFN Regression - Feature Importance (SHAP)', fontsize=14, fontweight='bold')
    plt.xlabel('mean(|SHAP value|) (average impact on model output magnitude)', fontsize=11)
    plt.tight_layout()
    bar_path = output_dir / 'shap_feature_importance_bar.png'
    plt.savefig(bar_path, dpi=150, bbox_inches='tight')
    plt.close()
    saved_files.append(bar_path)
    print(f"   ✓ Saved: {bar_path}")
    
    # 2. Beeswarm Plot (Global Feature Effects)
    print("\n2. Creating beeswarm plot...")
    fig, ax = plt.subplots(figsize=(10, 8))
    shap.summary_plot(shap_values, X_test_sample, show=False)
    plt.title('TabPFN Regression - Feature Impact Distribution (SHAP)', fontsize=14, fontweight='bold')
    plt.xlabel('SHAP value (impact on model output)', fontsize=11)
    plt.tight_layout()
    beeswarm_path = output_dir / 'shap_beeswarm.png'
    plt.savefig(beeswarm_path, dpi=150, bbox_inches='tight')
    plt.close()
    saved_files.append(beeswarm_path)
    print(f"   ✓ Saved: {beeswarm_path}")
    
    # 3. Dependence Plots for Top 2 Features
    print("\n3. Creating dependence plots for top 2 features (BMI and S5)...")
    fig, axes = plt.subplots(1, 2, figsize=(14, 5))
    
    # BMI
    plt.sca(axes[0])
    shap.dependence_plot("bmi", shap_values, X_test_sample, show=False, ax=axes[0])
    axes[0].set_title('BMI Impact on Prediction', fontsize=12, fontweight='bold')
    axes[0].set_xlabel('BMI (Body Mass Index)', fontsize=10)
    axes[0].set_ylabel('SHAP value for BMI', fontsize=10)
    
    # S5
    plt.sca(axes[1])
    shap.dependence_plot("s5", shap_values, X_test_sample, show=False, ax=axes[1])
    axes[1].set_title('S5 Impact on Prediction', fontsize=12, fontweight='bold')
    axes[1].set_xlabel('S5 (Blood Serum Measurement)', fontsize=10)
    axes[1].set_ylabel('SHAP value for S5', fontsize=10)
    
    plt.tight_layout()
    dep_path = output_dir / 'shap_dependence_top2.png'
    plt.savefig(dep_path, dpi=150, bbox_inches='tight')
    plt.close()
    saved_files.append(dep_path)
    print(f"   ✓ Saved: {dep_path}")
    
    return saved_files


